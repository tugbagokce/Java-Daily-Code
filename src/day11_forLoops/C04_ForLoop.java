package day11_forLoops;

public class C04_ForLoop {
    public static void main(String[] args) {



        int input=23;

        if (input>1){

        for (int i = input; i >=1 ; i--) {
            System.out.println(i+ " ");

        }
    }else{
            for (int i = input; i <=1 ; i++) {
                System.out.println(i + " ");
            }

            }
        }
}
